                "realmGroupsIdChildrenPost" -> {
                    val params = context.params
                    val realm = ApiHandlerUtils.searchStringInJson(params,"realm")
                    if(realm == null){
                        throw IllegalArgumentException("realm is required")
                    }
                    val id = ApiHandlerUtils.searchStringInJson(params,"id")
                    if(id == null){
                        throw IllegalArgumentException("id is required")
                    }
                    val groupRepresentationParam = ApiHandlerUtils.searchJsonObjectInJson(params,"body")
                    if (groupRepresentationParam == null) {
                        throw IllegalArgumentException("groupRepresentation is required")
                    }
                    val groupRepresentation = Gson().fromJson(groupRepresentationParam.encode(), GroupRepresentation::class.java)
                    GlobalScope.launch(vertx.dispatcher()){
                        val result = service.realmGroupsIdChildrenPost(realm,id,groupRepresentation,context)
                        val payload = JsonObject(Json.encode(result.payload)).toBuffer()
                        val res = OperationResponse(result.statusCode,result.statusMessage,payload,result.headers)
